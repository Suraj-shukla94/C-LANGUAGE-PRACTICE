#include <stdio.h>

int main()
{
    char ch = 'A';

    printf("Character = %c\n", ch);
    printf("ASCII Value = %d\n", ch);

    char ch2 = 65;

    printf("Character from ASCII = %c\n", ch2);

    return 0;
}

/* OUTPUT : 
Character = A
ASCII Value = 65
Character from ASCII = A */