#include <stdio.h>

int main() {
    int age = 18;
    float height = 5.9;
    char grade = 'A';
    double salary = 50000.75;
    long population = 1400000000;

    printf("Age = %d\n", age);
    printf("Height = %.1f\n", height);
    printf("Grade = %c\n", grade);
    printf("Salary = %.2lf\n", salary);
    printf("Population = %ld\n", population);

    return 0;
}
